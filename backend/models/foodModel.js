import mongoose from "mongoose";


// provides a model for the food items
const foodSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    price: {
        type: Number,
        required: true,
    },
    image: {
        type: String,
        required: true,
    },
    category: {
        type: String,
        required: true,
    },
})

const foodModel = mongoose.models.food || mongoose.model("food", foodSchema)
export default foodModel;